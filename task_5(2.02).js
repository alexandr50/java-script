function addition(num_1, num_2) {
    return num_1 + num_2
}
console.log(addition(2, 4))

function subtraction(num_1, num_2) {
    return num_1 - num_2
}
console.log(subtraction(4, 2))

function multiplication(num_1, num_2) {
    return num_1 * num_2
}
console.log(multiplication(4, 2))

function devision(num_1, num_2) {
    return num_1 / num_2
}
console.log(devision(6, 2))